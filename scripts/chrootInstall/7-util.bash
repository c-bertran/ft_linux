#!/bin/bash

mkdir utilLinux
tar -xvf util-linux-22.39.3.tar.xz -C utilLinux  --strip-components=1
cd utilLinux
mkdir -pv /var/lib/hwclock
./configure --libdir=/usr/lib     \
            --runstatedir=/run    \
            --disable-chfn-chsh   \
            --disable-login       \
            --disable-nologin     \
            --disable-su          \
            --disable-setpriv     \
            --disable-runuser     \
            --disable-pylibmount  \
            --disable-static      \
            --disable-liblastlog2 \
            --without-python      \
            ADJTIME_PATH=/var/lib/hwclock/adjtime \
            --docdir=/usr/share/doc/util-linux-2.40.2
make -j$(nproc)
make install
cd ..
rm -rf utilLinux
